package IO;

public class callmethodnoreturn {

	
	public  static void cal() {
			int a=1,b=2,c=0;
			c=a*b;
			System.out.println(c);
	}
	
	
	
		public  static void add()
		{
			int x=1,y=2,z=0;
			z=x+y;
			System.out.println(z);
			
		}
	
	public static void main(String[] args) {
		
		cal();
		callmethodnoreturn.add();
			
			
	}

}
